import 'package:flutter/material.dart';
import 'package:flutter/services.dart';
import 'package:flutter_animate/flutter_animate.dart';
import 'package:google_fonts/google_fonts.dart';
import 'package:pdf/pdf.dart';
import 'package:pdf/widgets.dart' as pw;
import 'package:printing/printing.dart';
import '../utils/app_theme.dart';

class MeasurementResultSheet extends StatefulWidget {
  final double distanceCm;
  final String mode;
  final String unit;
  final ValueChanged<String> onUnitToggle;
  final VoidCallback onShare;
  final ValueChanged<String> onSave;
  final VoidCallback onRetry;

  const MeasurementResultSheet({
    super.key,
    required this.distanceCm,
    required this.mode,
    required this.unit,
    required this.onUnitToggle,
    required this.onShare,
    required this.onSave,
    required this.onRetry,
  });

  @override
  State<MeasurementResultSheet> createState() => _MeasurementResultSheetState();
}

class _MeasurementResultSheetState extends State<MeasurementResultSheet> {
  late String _unit;
  final _labelCtrl = TextEditingController(text: 'Measurement');

  @override
  void initState() {
    super.initState();
    _unit = widget.unit;
  }

  double get _display => _unit == 'cm' ? widget.distanceCm : widget.distanceCm / 2.54;
  String get _displayStr => _unit == 'cm'
      ? '${_display.toStringAsFixed(1)} cm'
      : '${_display.toStringAsFixed(2)} in';

  Future<void> _exportPdf() async {
    final doc = pw.Document();
    doc.addPage(pw.Page(
      pageFormat: PdfPageFormat.a4,
      build: (pw.Context ctx) => pw.Column(
        crossAxisAlignment: pw.CrossAxisAlignment.start,
        children: [
          pw.Text('DimScan Measurement Report',
              style: pw.TextStyle(fontSize: 24, fontWeight: pw.FontWeight.bold)),
          pw.SizedBox(height: 20),
          pw.Divider(),
          pw.SizedBox(height: 16),
          pw.Text('Label: ${_labelCtrl.text}', style: const pw.TextStyle(fontSize: 14)),
          pw.SizedBox(height: 8),
          pw.Text('Mode: ${widget.mode.toUpperCase()}', style: const pw.TextStyle(fontSize: 14)),
          pw.SizedBox(height: 8),
          pw.Text('Date: ${DateTime.now().toString().substring(0, 16)}', style: const pw.TextStyle(fontSize: 14)),
          pw.SizedBox(height: 24),
          pw.Container(
            padding: const pw.EdgeInsets.all(20),
            decoration: pw.BoxDecoration(border: pw.Border.all()),
            child: pw.Column(children: [
              pw.Text('Measurement Result', style: pw.TextStyle(fontSize: 16, fontWeight: pw.FontWeight.bold)),
              pw.SizedBox(height: 12),
              pw.Text(_displayStr, style: pw.TextStyle(fontSize: 36, fontWeight: pw.FontWeight.bold)),
              pw.SizedBox(height: 8),
              pw.Text('= ${widget.distanceCm.toStringAsFixed(1)} cm'
                  ' = ${(widget.distanceCm / 2.54).toStringAsFixed(2)} in'
                  ' = ${(widget.distanceCm * 10).toStringAsFixed(0)} mm',
                  style: const pw.TextStyle(fontSize: 12)),
            ]),
          ),
          pw.SizedBox(height: 16),
          pw.Text('Generated by DimScan', style: const pw.TextStyle(fontSize: 10, color: PdfColors.grey)),
        ],
      ),
    ));
    await Printing.layoutPdf(onLayout: (fmt) async => doc.save());
  }

  @override
  Widget build(BuildContext context) {
    return Container(
      decoration: const BoxDecoration(
        color: AppTheme.surface,
        borderRadius: BorderRadius.vertical(top: Radius.circular(28)),
      ),
      padding: EdgeInsets.fromLTRB(22, 10, 22, MediaQuery.of(context).viewInsets.bottom + 22),
      child: SingleChildScrollView(
        child: Column(mainAxisSize: MainAxisSize.min, children: [
          // Handle
          Container(width: 40, height: 4, margin: const EdgeInsets.only(bottom: 20),
            decoration: BoxDecoration(color: AppTheme.textDim, borderRadius: BorderRadius.circular(2))),

          // Big readout
          _buildResult(),
          const SizedBox(height: 18),

          // Conversions row
          _buildConversions(),
          const SizedBox(height: 16),

          // Label
          TextField(
            controller: _labelCtrl,
            style: GoogleFonts.spaceGrotesk(color: AppTheme.textPrimary, fontSize: 15),
            decoration: InputDecoration(
              labelText: 'Label',
              labelStyle: GoogleFonts.spaceGrotesk(color: AppTheme.textDim, fontSize: 13),
              prefixIcon: const Icon(Icons.label_outline_rounded, color: AppTheme.textDim, size: 20),
              filled: true, fillColor: AppTheme.surfaceElevated,
              enabledBorder: OutlineInputBorder(borderRadius: BorderRadius.circular(14),
                borderSide: const BorderSide(color: AppTheme.divider)),
              focusedBorder: OutlineInputBorder(borderRadius: BorderRadius.circular(14),
                borderSide: const BorderSide(color: AppTheme.accent, width: 1.5)),
            ),
          ),
          const SizedBox(height: 16),

          // Action buttons
          _buildActions(),
          const SizedBox(height: 6),
        ]),
      ),
    ).animate().slideY(begin: 0.3, duration: 400.ms, curve: Curves.easeOut);
  }

  Widget _buildResult() {
    return Column(children: [
      Text('RESULT', style: GoogleFonts.spaceGrotesk(
        fontSize: 10, color: AppTheme.textDim, letterSpacing: 3)),
      const SizedBox(height: 10),
      Container(
        padding: const EdgeInsets.symmetric(horizontal: 28, vertical: 18),
        decoration: BoxDecoration(
          color: AppTheme.accentGlow,
          borderRadius: BorderRadius.circular(20),
          border: Border.all(color: AppTheme.accent.withOpacity(0.3)),
          boxShadow: [BoxShadow(color: AppTheme.accent.withOpacity(0.15), blurRadius: 24, spreadRadius: 4)],
        ),
        child: Row(mainAxisSize: MainAxisSize.min, crossAxisAlignment: CrossAxisAlignment.end, children: [
          Text(
            _unit == 'cm' ? _display.toStringAsFixed(1) : _display.toStringAsFixed(2),
            style: GoogleFonts.orbitron(fontSize: 52, fontWeight: FontWeight.w700, color: AppTheme.accent, height: 1),
          ),
          const SizedBox(width: 8),
          Padding(
            padding: const EdgeInsets.only(bottom: 8),
            child: Text(_unit, style: GoogleFonts.orbitron(fontSize: 20, color: AppTheme.accentDim)),
          ),
        ]),
      ),
      const SizedBox(height: 12),
      // Unit chips
      Row(mainAxisAlignment: MainAxisAlignment.center, children: [
        _UnitChip(label: 'CM', selected: _unit == 'cm',
          onTap: () { setState(() => _unit = 'cm'); widget.onUnitToggle('cm'); }),
        const SizedBox(width: 8),
        _UnitChip(label: 'INCHES', selected: _unit == 'in',
          onTap: () { setState(() => _unit = 'in'); widget.onUnitToggle('in'); }),
      ]),
    ]);
  }

  Widget _buildConversions() {
    final conversions = [
      ('mm', (widget.distanceCm * 10).toStringAsFixed(0)),
      ('cm', widget.distanceCm.toStringAsFixed(1)),
      ('in', (widget.distanceCm / 2.54).toStringAsFixed(2)),
      ('ft', (widget.distanceCm / 30.48).toStringAsFixed(3)),
      ('m',  (widget.distanceCm / 100).toStringAsFixed(3)),
    ];
    return Container(
      padding: const EdgeInsets.all(14),
      decoration: BoxDecoration(color: AppTheme.surfaceElevated, borderRadius: BorderRadius.circular(16)),
      child: Column(children: [
        Text('UNIT CONVERSIONS', style: GoogleFonts.spaceGrotesk(
          fontSize: 9, color: AppTheme.textDim, letterSpacing: 2)),
        const SizedBox(height: 10),
        Row(children: conversions.map((c) => Expanded(child: Column(children: [
          Text(c.$2, style: GoogleFonts.orbitron(fontSize: 12, fontWeight: FontWeight.w600, color: AppTheme.textPrimary)),
          Text(c.$1, style: GoogleFonts.spaceGrotesk(fontSize: 10, color: AppTheme.textDim, letterSpacing: 1)),
        ]))).toList()),
      ]),
    );
  }

  Widget _buildActions() {
    return Column(children: [
      // Row 1
      Row(children: [
        Expanded(child: OutlinedButton.icon(
          onPressed: () { Navigator.pop(context); widget.onRetry(); },
          icon: const Icon(Icons.refresh_rounded, size: 16, color: AppTheme.textSecondary),
          label: Text('Retry', style: GoogleFonts.spaceGrotesk(color: AppTheme.textSecondary, fontWeight: FontWeight.w600)),
          style: OutlinedButton.styleFrom(
            padding: const EdgeInsets.symmetric(vertical: 13),
            side: const BorderSide(color: AppTheme.divider),
            shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(14))),
        )),
        const SizedBox(width: 10),
        // Copy
        _iconActionBtn(Icons.copy_rounded, () {
          Clipboard.setData(ClipboardData(text: _displayStr));
          ScaffoldMessenger.of(context).showSnackBar(SnackBar(
            content: Text('Copied: $_displayStr', style: GoogleFonts.spaceGrotesk(color: AppTheme.textPrimary)),
            backgroundColor: AppTheme.surface, behavior: SnackBarBehavior.floating,
            shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(12)),
          ));
        }),
        const SizedBox(width: 10),
        // Share screenshot
        _iconActionBtn(Icons.share_rounded, () { Navigator.pop(context); widget.onShare(); }),
        const SizedBox(width: 10),
        // PDF export
        _iconActionBtn(Icons.picture_as_pdf_rounded, _exportPdf, color: AppTheme.error),
      ]),
      const SizedBox(height: 10),
      // Save button full width
      SizedBox(
        width: double.infinity,
        child: ElevatedButton.icon(
          onPressed: () {
            final label = _labelCtrl.text.trim().isEmpty ? 'Measurement' : _labelCtrl.text.trim();
            widget.onSave(label);
            Navigator.pop(context);
          },
          icon: const Icon(Icons.bookmark_rounded, size: 18),
          label: Text('Save Measurement', style: GoogleFonts.spaceGrotesk(fontWeight: FontWeight.w700, fontSize: 15)),
          style: ElevatedButton.styleFrom(
            backgroundColor: AppTheme.accent, foregroundColor: AppTheme.background,
            padding: const EdgeInsets.symmetric(vertical: 15),
            shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(14)),
            elevation: 0,
          ),
        ),
      ),
    ]);
  }

  Widget _iconActionBtn(IconData icon, VoidCallback onTap, {Color color = AppTheme.textSecondary}) {
    return GestureDetector(
      onTap: onTap,
      child: Container(
        width: 46, height: 46,
        decoration: BoxDecoration(
          color: AppTheme.surfaceElevated,
          borderRadius: BorderRadius.circular(12),
          border: Border.all(color: AppTheme.divider),
        ),
        child: Icon(icon, color: color, size: 20),
      ),
    );
  }
}

class _UnitChip extends StatelessWidget {
  final String label;
  final bool selected;
  final VoidCallback onTap;
  const _UnitChip({required this.label, required this.selected, required this.onTap});
  @override
  Widget build(BuildContext context) => GestureDetector(
    onTap: onTap,
    child: AnimatedContainer(
      duration: 200.ms,
      padding: const EdgeInsets.symmetric(horizontal: 20, vertical: 8),
      decoration: BoxDecoration(
        color: selected ? AppTheme.accent : AppTheme.surfaceElevated,
        borderRadius: BorderRadius.circular(20),
        border: Border.all(color: selected ? AppTheme.accent : AppTheme.divider),
      ),
      child: Text(label, style: GoogleFonts.spaceGrotesk(
        fontSize: 12, fontWeight: FontWeight.w700, letterSpacing: 1,
        color: selected ? AppTheme.background : AppTheme.textSecondary)),
    ),
  );
}
